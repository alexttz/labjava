public class PositivoNegativo{
    public static void main(String[]args){

        int numero = 5;

        if(numero < 0){
            System.out.println("O numero é negativo! ");
        }else if(numero > 0){
            System.out.println("O numero é positivo! ");
        }else{
            System.out.println("O numero é igual a zero. ");
        }
    }
}